"""StressTestMetrics: aggregate scoring and degradation curve computation.

Computes per-scenario metrics from judge results, telemetry, and cost data.
Generates degradation curves showing quality vs. fault probability.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any


@dataclass
class StressTestMetrics:
    """Aggregated metrics for a single stress test run."""

    scenario_name: str = ""
    fault_type: str = ""
    fault_probability: float = 0.0

    # Quality scores (from judge)
    baseline_score: float = 0.0
    stressed_score: float = 0.0
    score_delta: float = 0.0
    degradation_pct: float = 0.0

    # Per-criterion scores
    baseline_scores: dict[str, float] = field(default_factory=dict)
    stressed_scores: dict[str, float] = field(default_factory=dict)
    criterion_deltas: dict[str, float] = field(default_factory=dict)

    # Cost metrics
    baseline_cost_usd: float = 0.0
    stressed_cost_usd: float = 0.0
    cost_overhead_ratio: float = 1.0

    # Token metrics
    baseline_tokens: int = 0
    stressed_tokens: int = 0

    # Latency metrics
    baseline_latency_ms: float = 0.0
    stressed_latency_ms: float = 0.0

    # Fault stats
    faults_triggered: int = 0
    agents_affected: list[str] = field(default_factory=list)

    def to_dict(self) -> dict[str, Any]:
        return {
            "scenario_name": self.scenario_name,
            "fault_type": self.fault_type,
            "fault_probability": self.fault_probability,
            "quality": {
                "baseline_score": self.baseline_score,
                "stressed_score": self.stressed_score,
                "score_delta": self.score_delta,
                "degradation_pct": self.degradation_pct,
                "criterion_deltas": self.criterion_deltas,
            },
            "cost": {
                "baseline_usd": self.baseline_cost_usd,
                "stressed_usd": self.stressed_cost_usd,
                "overhead_ratio": self.cost_overhead_ratio,
            },
            "tokens": {
                "baseline": self.baseline_tokens,
                "stressed": self.stressed_tokens,
            },
            "latency": {
                "baseline_ms": self.baseline_latency_ms,
                "stressed_ms": self.stressed_latency_ms,
            },
            "faults": {
                "triggered": self.faults_triggered,
                "agents_affected": self.agents_affected,
            },
        }


@dataclass
class DegradationPoint:
    """A single point on the degradation curve."""

    fault_probability: float
    quality_score: float
    cost_usd: float
    tokens: int
    latency_ms: float


@dataclass
class DegradationCurve:
    """Quality/cost degradation curve across fault probability levels.

    Generated by sweep mode: runs the same scenario at multiple fault
    probability levels (e.g., 0.1, 0.3, 0.5, 0.7, 1.0) and plots
    quality and cost against fault intensity.
    """

    scenario_name: str
    fault_type: str
    baseline_score: float
    baseline_cost_usd: float
    points: list[DegradationPoint] = field(default_factory=list)

    def add_point(
        self,
        fault_probability: float,
        quality_score: float,
        cost_usd: float,
        tokens: int = 0,
        latency_ms: float = 0.0,
    ) -> None:
        self.points.append(
            DegradationPoint(
                fault_probability=fault_probability,
                quality_score=quality_score,
                cost_usd=cost_usd,
                tokens=tokens,
                latency_ms=latency_ms,
            )
        )
        self.points.sort(key=lambda p: p.fault_probability)

    @property
    def probabilities(self) -> list[float]:
        return [p.fault_probability for p in self.points]

    @property
    def quality_scores(self) -> list[float]:
        return [p.quality_score for p in self.points]

    @property
    def costs(self) -> list[float]:
        return [p.cost_usd for p in self.points]

    def quality_at(self, probability: float) -> float | None:
        """Interpolate quality score at a given fault probability."""
        for p in self.points:
            if abs(p.fault_probability - probability) < 0.001:
                return p.quality_score
        return None

    def half_degradation_point(self) -> float | None:
        """Find the fault probability at which quality drops to 50% of baseline."""
        threshold = self.baseline_score * 0.5
        for p in self.points:
            if p.quality_score <= threshold:
                return p.fault_probability
        return None

    def resilience_score(self) -> float:
        """Compute overall resilience: area under the quality curve normalized by baseline.

        1.0 = perfectly resilient (no degradation at any fault level)
        0.0 = completely fragile (immediate total failure)
        """
        if not self.points or self.baseline_score == 0:
            return 0.0

        total = sum(p.quality_score for p in self.points)
        max_possible = self.baseline_score * len(self.points)
        return round(total / max_possible, 4) if max_possible > 0 else 0.0

    def cost_efficiency_at(self, probability: float) -> float | None:
        """Compute quality-per-dollar at a given fault probability."""
        for p in self.points:
            if abs(p.fault_probability - probability) < 0.001:
                return round(p.quality_score / p.cost_usd, 4) if p.cost_usd > 0 else None
        return None

    def to_dict(self) -> dict[str, Any]:
        return {
            "scenario_name": self.scenario_name,
            "fault_type": self.fault_type,
            "baseline_score": self.baseline_score,
            "baseline_cost_usd": self.baseline_cost_usd,
            "resilience_score": self.resilience_score(),
            "half_degradation_point": self.half_degradation_point(),
            "points": [
                {
                    "fault_probability": p.fault_probability,
                    "quality_score": p.quality_score,
                    "cost_usd": p.cost_usd,
                    "tokens": p.tokens,
                    "latency_ms": p.latency_ms,
                }
                for p in self.points
            ],
        }


def compute_metrics(
    scenario_name: str,
    fault_type: str,
    fault_probability: float,
    baseline_judge_result: Any,
    stressed_judge_result: Any,
    baseline_telemetry_summary: dict[str, Any] | None = None,
    stressed_telemetry_summary: dict[str, Any] | None = None,
    cost_overhead: dict[str, Any] | None = None,
) -> StressTestMetrics:
    """Compute StressTestMetrics from judge results and telemetry."""
    metrics = StressTestMetrics(
        scenario_name=scenario_name,
        fault_type=fault_type,
        fault_probability=fault_probability,
    )

    if baseline_judge_result:
        metrics.baseline_score = baseline_judge_result.overall_score
        metrics.baseline_scores = dict(baseline_judge_result.scores)

    if stressed_judge_result:
        metrics.stressed_score = stressed_judge_result.overall_score
        metrics.stressed_scores = dict(stressed_judge_result.scores)

    metrics.score_delta = round(metrics.stressed_score - metrics.baseline_score, 4)
    if metrics.baseline_score > 0:
        metrics.degradation_pct = round(
            (1 - metrics.stressed_score / metrics.baseline_score) * 100, 2
        )

    for criterion in set(metrics.baseline_scores) | set(metrics.stressed_scores):
        b = metrics.baseline_scores.get(criterion, 0.0)
        s = metrics.stressed_scores.get(criterion, 0.0)
        metrics.criterion_deltas[criterion] = round(s - b, 4)

    if cost_overhead:
        metrics.baseline_cost_usd = cost_overhead.get("baseline_cost_usd", 0.0)
        metrics.stressed_cost_usd = cost_overhead.get("stressed_cost_usd", 0.0)
        metrics.cost_overhead_ratio = cost_overhead.get("overhead_ratio", 1.0)
        metrics.baseline_tokens = cost_overhead.get("baseline_tokens", 0)
        metrics.stressed_tokens = cost_overhead.get("stressed_tokens", 0)

    if baseline_telemetry_summary:
        metrics.baseline_latency_ms = baseline_telemetry_summary.get("total_latency_ms", 0.0)

    if stressed_telemetry_summary:
        metrics.stressed_latency_ms = stressed_telemetry_summary.get("total_latency_ms", 0.0)
        metrics.faults_triggered = stressed_telemetry_summary.get("faults_triggered", 0)
        metrics.agents_affected = stressed_telemetry_summary.get("agents_observed", [])

    return metrics
